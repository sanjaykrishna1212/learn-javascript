// NUMBER METHODS
/**
 * 1. Math.round()
 * @description Rounds to the nearest integer.
 */
console.log(Math.round(4.4)); // 4
console.log(Math.round(4.6)); // 5

/**
 * 2. Math.floor()
 * @description Rounds down to the nearest integer.
 */
console.log(Math.floor(4.9)); // 4
console.log(Math.floor(-4.9)); // -5

/**
 * 3. Math.ceil()
 * @description Rounds up to the nearest integer.
 */
console.log(Math.ceil(4.1)); // 5
console.log(Math.ceil(-4.1)); // -4

/**
 * 4. Math.max()
 * @description Returns the largest number.
 */
console.log(Math.max(3, 7, 1)); // 7

/**
 * 5. Math.min()
 * @description Rounds down to the nearest integer.
 */
console.log(Math.min(3, 7, 1)); // 1

/**
 * 6. Math.random()
 * @description Returns a random number between 0 (inclusive) and 1 (exclusive).
 */
console.log(Math.floor(Math.random()*100)); // e.g., 0.4567
console.log(Math.floor(Math.random() * 10)); // Random 0–9

/**
 * 7.  Math.pow()
 * Raises a number to the power of another.
 */
console.log(Math.pow(2, 3)); // 8  (2³)

/**
 * 8. Math.sqrt()
 * Returns the square root.
 */
console.log(Math.sqrt(16)); // 4

/**
 * 9. Math.abs()
 * Returns the absolute value (removes negative sign).
 */
console.log(Math.abs(-7)); // 7
